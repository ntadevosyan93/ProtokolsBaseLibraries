CREATE VIEW dbo.View$RM$OS_MOVE_ID
AS
SELECT     (SELECT     TOP (1) id
                       FROM          dbo.RM$OS$MOVE AS osm
                       WHERE      (idm_rm_os = os.id) AND (del = 0)
                       ORDER BY id DESC) AS last_move_id, id, date_in, user_in, idm_org, idm_type, idm_addr, idm_mol, place, text, date_start, serial_num, inv_num, doc_date_purchase, 
                      doc_num_purchase, idm_partner, del, idm_invoice, status, idm_partner_service, inv_num_extended, idm_head_org, idm_otdel, idm_responsible, user_cor, date_cor, 
                      date_release, date_expired, date_check, idm_priority
FROM         dbo.RM$OS AS os
GO
